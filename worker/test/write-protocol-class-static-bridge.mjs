import { writeFileSync } from "node:fs";
import { protocolClassStaticSurface } from "./protocol-class-static-surface.mjs";

const outputPath = process.argv[2] ?? "worker/native/generated_class_static_bridge.h";
const surface = protocolClassStaticSurface();
const files = [...new Set(surface.classes.map((entry) => entry.file))].sort();

function scalarParams(method) {
  return method.parameters.filter((param) => !["state", "stateStruct"].includes(param.source));
}

function castArg(param, index) {
  const type = param.scalarType;
  if (type === "bool") return `args[${index}] != 0`;
  return `static_cast<${type}>(args[${index}])`;
}

function resultExpression(method) {
  let scalarIndex = 0;
  let structIndex = 0;
  const callArgs = method.parameters.map((param) => {
    if (param.source === "state") return "state.data()";
    if (param.source === "stateStruct") {
      const arg = `rawStruct${structIndex}`;
      structIndex++;
      return arg;
    }
    const arg = castArg(param, scalarIndex);
    scalarIndex++;
    return arg;
  });
  const call = `${method.className}::${method.name}(${callArgs.join(", ")})`;
  if (method.scalarReturnType === "bool") {
    return `const bool value = ${call};
      out << "\\"result\\":" << (value ? "true" : "false");`;
  }
  if (method.scalarReturnType === "float" || method.scalarReturnType === "double") {
    return `const double value = static_cast<double>(${call});
      out << "\\"result\\":" << value;`;
  }
  return `const long long value = static_cast<long long>(${call});
      out << "\\"result\\":" << value;`;
}

function stateStructSetup(method) {
  let structIndex = 0;
  return method.parameters.map((param) => {
    if (param.source !== "stateStruct") return "";
    const name = `rawStruct${structIndex}`;
    structIndex++;
    return `      ${param.structType} ${name} = {};
      std::memcpy(${name}.raw, state.data(), std::min(state.size(), sizeof(${name}.raw)));`;
  }).filter(Boolean).join("\n");
}

function methodCase(className, method) {
  const enriched = { ...method, className };
  const needsState = method.parameters.some((param) => ["state", "stateStruct"].includes(param.source));
  const stateCheck = needsState
    ? `      if (state.empty()) {
        result = errorJson("${className}.${method.name} requires state bytes");
        return true;
      }
`
    : "";
  return `    if (!std::strcmp(methodName, "${method.name}") && args.size() == ${scalarParams(method).length}) {
${stateCheck}      (void)state;
      std::ostringstream out;
      out << "{\\"kind\\":\\"classStatic\\",";
      out << "\\"className\\":\\"${className}\\",";
      out << "\\"method\\":\\"${method.name}\\",";
${stateStructSetup(method)}
      ${resultExpression(enriched)}
      out << "}";
      result = out.str();
      return true;
    }`;
}

function classCase(entry) {
  return `  if (!std::strcmp(className, "${entry.className}")) {
${entry.methods.map((method) => methodCase(entry.className, method)).join("\n")}
    result = errorJson("unsupported protocol class static method");
    return true;
  }`;
}

const content = `// Generated by worker/test/write-protocol-class-static-bridge.mjs.
// Do not edit by hand.

${files.map((file) => `#include "${file}"`).join("\n")}

namespace {

bool generateClassStaticJson(const char *className,
                             const std::vector<uint8_t> &state,
                             const char *methodName,
                             const char *argsCsv,
                             std::string &result) {
  if (className == nullptr || *className == '\\0') {
    result = errorJson("className is required");
    return true;
  }
  if (methodName == nullptr || *methodName == '\\0') {
    result = errorJson("method is required");
    return true;
  }
  const std::vector<uint64_t> args = parseWideNumbers(argsCsv);
${surface.classes.map(classCase).join("\n")}
  return false;
}

}  // namespace
`;

writeFileSync(outputPath, content);

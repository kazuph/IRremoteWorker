import { writeFileSync } from "node:fs";
import { protocolClassRawSurface } from "./protocol-class-raw-surface.mjs";

const outputPath = process.argv[2] ?? "worker/native/generated_class_raw_bridge.h";
const surface = protocolClassRawSurface();
const files = [...new Set(surface.classes.map((entry) => entry.file))].sort();

function byteStateLength(entry) {
  if (entry.rawLengthMethod) return `static_cast<size_t>(ac.${entry.rawLengthMethod}())`;
  if (entry.stateLengthExpression) return `static_cast<size_t>(${entry.stateLengthExpression})`;
  return "state.size()";
}

function byteCase(entry) {
  const setRawCall = entry.setRawLengthArg ? "ac.setRaw(state.data(), state.size());" : "ac.setRaw(state.data());";
  const lengthExpression = `state.empty() ? ${byteStateLength(entry)} : state.size()`;
  const sendCall = entry.sendVia === "irsend"
    ? `CapturingIRsend irsend(0);
    timingList.clear();
    irsend.${entry.sendMethod}(ac.getRaw(), ${lengthExpression}, repeat);`
    : `timingList.clear();
    ac.send(repeat);`;
  return `  if (!std::strcmp(className, "${entry.className}")) {
    ${entry.className} ac(0);
    if (!state.empty()) {
      ${setRawCall}
    }
    ${sendCall}
    std::ostringstream out;
    out << "{\\"kind\\":\\"class\\",";
    out << "\\"className\\":\\"${entry.className}\\",";
    out << "\\"rawType\\":\\"bytes\\",";
    out << "\\"sendVia\\":\\"${entry.sendVia ?? "class"}\\",";
    out << "\\"repeat\\":" << repeat << ",";
    out << "\\"frequency\\":38000,";
    appendState(out, ac.getRaw(), ${lengthExpression});
    out << ",";
    appendTimingList(out, "raw");
    out << "}";
    result = out.str();
    return true;
  }`;
}

function valueCase(entry) {
  const type = entry.rawType === "uint32" ? "uint32_t" : "uint64_t";
  return `  if (!std::strcmp(className, "${entry.className}")) {
    ${entry.className} ac(0);
    ac.setRaw(static_cast<${type}>(rawValue));
    timingList.clear();
    ac.send(repeat);
    std::ostringstream out;
    out << "{\\"kind\\":\\"class\\",";
    out << "\\"className\\":\\"${entry.className}\\",";
    out << "\\"rawType\\":\\"${entry.rawType}\\",";
    out << "\\"repeat\\":" << repeat << ",";
    out << "\\"frequency\\":38000,";
    out << "\\"value\\":\\"" << static_cast<unsigned long long>(ac.getRaw()) << "\\",";
    out << "\\"valueHex\\":\\"" << hex64(static_cast<uint64_t>(ac.getRaw())) << "\\",";
    appendTimingList(out, "raw");
    out << "}";
    result = out.str();
    return true;
  }`;
}

const content = `// Generated by worker/test/write-protocol-class-raw-bridge.mjs.
// Do not edit by hand.

${files.map((file) => `#include "${file}"`).join("\n")}

namespace {

bool generateClassRawJson(const char *className,
                          const std::vector<uint8_t> &state,
                          uint64_t rawValue,
                          uint16_t repeat,
                          std::string &result) {
  if (className == nullptr || *className == '\\0') {
    result = errorJson("className is required");
    return true;
  }
${surface.classes.map((entry) => entry.rawType === "bytes" ? byteCase(entry) : valueCase(entry)).join("\n")}
  return false;
}

}  // namespace
`;

writeFileSync(outputPath, content);

import { writeFileSync } from "node:fs";
import { protocolClassMethodSurface } from "./protocol-class-method-surface.mjs";

const outputPath = process.argv[2] ?? "worker/native/generated_class_method_bridge.h";
const surface = protocolClassMethodSurface();
const files = [...new Set(surface.classes.map((entry) => entry.file))].sort();

function castArg(param, index) {
  if (param.source === "argsSet") return `arg${index}`;
  const type = param.scalarType;
  if (type === "bool") return `args[${index}] != 0`;
  return `static_cast<${type}>(args[${index}])`;
}

function argumentSetup(method) {
  return method.parameters.map((param, index) => {
    if (param.source !== "argsSet") return "";
    return `      std::set<${param.setElementType}> arg${index};
      for (const uint64_t arg : args) {
        arg${index}.insert(static_cast<${param.setElementType}>(arg));
      }`;
  }).filter(Boolean).join("\n");
}

function argsCondition(method) {
  const hasSetArg = method.parameters.some((param) => param.source === "argsSet");
  if (hasSetArg) return "true";
  return `args.size() == ${method.parameters.length}`;
}

function resultExpression(method) {
  const type = method.scalarReturnType;
  const call = `ac.${method.name}(${method.parameters.map((param, index) => castArg(param, index)).join(", ")})`;
  if (type === "void") {
    return `${call};
      out << "\\"result\\":null,";`;
  }
  if (method.resultKind === "set") {
    return `const auto value = ${call};
      out << "\\"result\\":[";
      size_t valueIndex = 0;
      for (const auto item : value) {
        if (valueIndex++ > 0) out << ",";
        out << static_cast<long long>(item);
      }
      out << "],";`;
  }
  if (type === "bool") {
    return `const bool value = ${call};
      out << "\\"result\\":" << (value ? "true" : "false") << ",";`;
  }
  if (type === "float" || type === "double") {
    return `const double value = static_cast<double>(${call});
      out << "\\"result\\":" << value << ",";`;
  }
  return `const long long value = static_cast<long long>(${call});
      out << "\\"result\\":" << value << ",";`;
}

function methodCase(method, rawType) {
  return `    if (!std::strcmp(methodName, "${method.name}") && ${argsCondition(method)}) {
      std::ostringstream out;
      out << "{\\"kind\\":\\"classMethod\\",";
      out << "\\"className\\":\\"CLASS_NAME\\",";
      out << "\\"method\\":\\"${method.name}\\",";
      out << "\\"rawType\\":\\"${rawType}\\",";
${argumentSetup(method)}
      ${resultExpression(method)}
      FINAL_RAW
      out << "}";
      result = out.str();
      return true;
    }`;
}

function byteStateLength(entry) {
  if (entry.rawLengthMethod) return `static_cast<size_t>(ac.${entry.rawLengthMethod}())`;
  if (entry.stateLengthExpression) return `static_cast<size_t>(${entry.stateLengthExpression})`;
  return "state.size()";
}

function classCase(entry) {
  const setRawCall = entry.setRawLengthArg ? "ac.setRaw(state.data(), state.size());" : "ac.setRaw(state.data());";
  const setup = entry.rawType === "bytes"
    ? `    ${entry.className} ac(0);
    if (!state.empty()) {
      ${setRawCall}
    }`
    : `    ${entry.className} ac(0);
    ac.setRaw(static_cast<${entry.rawType === "uint32" ? "uint32_t" : "uint64_t"}>(rawValue));`;
  const finalRaw = entry.rawType === "bytes"
    ? `appendState(out, ac.getRaw(), state.empty() ? ${byteStateLength(entry)} : state.size());`
    : `out << "\\"value\\":\\"" << static_cast<unsigned long long>(ac.getRaw()) << "\\",";
      out << "\\"valueHex\\":\\"" << hex64(static_cast<uint64_t>(ac.getRaw())) << "\\"";`;
  return `  if (!std::strcmp(className, "${entry.className}")) {
${setup}
${entry.methods.map((method) => methodCase(method, entry.rawType).replaceAll("CLASS_NAME", entry.className).replace("FINAL_RAW", finalRaw)).join("\n")}
    result = errorJson("unsupported protocol class scalar method");
    return true;
  }`;
}

const content = `// Generated by worker/test/write-protocol-class-method-bridge.mjs.
// Do not edit by hand.

${files.map((file) => `#include "${file}"`).join("\n")}
#include <set>

namespace {

bool generateClassMethodJson(const char *className,
                             const std::vector<uint8_t> &state,
                             uint64_t rawValue,
                             const char *methodName,
                             const char *argsCsv,
                             std::string &result) {
  if (className == nullptr || *className == '\\0') {
    result = errorJson("className is required");
    return true;
  }
  if (methodName == nullptr || *methodName == '\\0') {
    result = errorJson("method is required");
    return true;
  }
  const std::vector<uint64_t> args = parseWideNumbers(argsCsv);
${surface.classes.map(classCase).join("\n")}
  return false;
}

}  // namespace
`;

writeFileSync(outputPath, content);

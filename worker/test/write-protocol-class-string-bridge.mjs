import { writeFileSync } from "node:fs";
import { protocolClassStringSurface } from "./protocol-class-string-surface.mjs";

const outputPath = process.argv[2] ?? "worker/native/generated_class_string_bridge.h";
const surface = protocolClassStringSurface();
const files = [...new Set(surface.classes.map((entry) => entry.file))].sort();

function byteStateLength(entry) {
  if (entry.rawLengthMethod) return `static_cast<size_t>(ac.${entry.rawLengthMethod}())`;
  if (entry.stateLengthExpression) return `static_cast<size_t>(${entry.stateLengthExpression})`;
  return "state.size()";
}

function classCase(entry) {
  const setRawCall = entry.setRawLengthArg ? "ac.setRaw(state.data(), state.size());" : "ac.setRaw(state.data());";
  const setup = entry.rawType === "bytes"
    ? `    ${entry.className} ac(0);
    if (!state.empty()) {
      ${setRawCall}
    }`
    : `    ${entry.className} ac(0);
    ac.setRaw(static_cast<${entry.rawType === "uint32" ? "uint32_t" : "uint64_t"}>(rawValue));`;
  const finalRaw = entry.rawType === "bytes"
    ? `appendState(out, ac.getRaw(), state.empty() ? ${byteStateLength(entry)} : state.size());`
    : `out << "\\"value\\":\\"" << static_cast<unsigned long long>(ac.getRaw()) << "\\",";
    out << "\\"valueHex\\":\\"" << hex64(static_cast<uint64_t>(ac.getRaw())) << "\\"";`;
  return `  if (!std::strcmp(className, "${entry.className}")) {
${setup}
    const std::string text = std::string(ac.toString().c_str());
    std::ostringstream out;
    out << "{\\"kind\\":\\"classString\\",";
    out << "\\"className\\":\\"${entry.className}\\",";
    out << "\\"rawType\\":\\"${entry.rawType}\\",";
    out << "\\"result\\":\\"" << escapeJson(text) << "\\",";
    ${finalRaw}
    out << "}";
    result = out.str();
    return true;
  }`;
}

const content = `// Generated by worker/test/write-protocol-class-string-bridge.mjs.
// Do not edit by hand.

${files.map((file) => `#include "${file}"`).join("\n")}

namespace {

bool generateClassStringJson(const char *className,
                             const std::vector<uint8_t> &state,
                             uint64_t rawValue,
                             std::string &result) {
  if (className == nullptr || *className == '\\0') {
    result = errorJson("className is required");
    return true;
  }
${surface.classes.map(classCase).join("\n")}
  return false;
}

}  // namespace
`;

writeFileSync(outputPath, content);
